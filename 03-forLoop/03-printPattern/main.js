// ให้เขียนโค้ดแสดงรูปดอกจันตามรูปแบบด้านล่าง โดยแสดงผลออกมาใน console

// ```
// *
// *	*
// *	*	*
// *	*	*	*
// ```

// หมายเหตุ : คำสั่งขึ้นบรรทัดใหม่ใน console ให้ใช้ \n

let str = "";

for(let i = 1; i <= 4; i++) {
    for(let j = 1; j <= i; j++) {
       str += "*" 
    };
    str += "\n";
};

console.log(str);